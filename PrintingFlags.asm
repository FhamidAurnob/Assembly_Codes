INCLUDE "EMU8086.INC"
.MODEL SMALL
.STACK 100H

.DATA



.CODE

MAIN PROC
	
	MOV AX, @DATA
	MOV DS, AX
	
	MOV BL, 0FFH
	INC BL
	
	MOV AH, 2
	
	;PRINTING CARRY FLAG
	
	PRINT "CF: "
	JC CARRY
	
	PRINTN "0"
	JMP ZF
	
	CARRY:
	
	PRINTN "1"
	
	;PRINTING ZERO FLAG
	
	ZF:
	
	PRINT "ZF: "
	JZ ZERO
	
	PRINTN "0"
	JMP SF
	
	ZERO:
	
	PRINTN "1"
	
	;PRINTING SIGN FLAG
	
	SF:
	
	PRINT "SF: "
	JS SIGN
	
	PRINTN "0"
	JMP OF
	
	SIGN:
	
	PRINTN "1"
	
	;PRINTING OVERFLOW FLAG
	
	OF:
	
	PRINT "OF: "
	JO OVERFLOW
	
	PRINTN "0"
	JMP PF
	
	OVERFLOW:
	
	PRINTN "1"
	
	;PRINTING PARITY FLAG
	
	PF:
	
	PRINT "PF: "
	JP PARITY
	
	PRINTN "0"
	JMP AF
	
	PARITY:
	
	PRINTN "1"
	
	;PRINTING AUXILIARY FLAG
	
	AF:
	
	PRINT "AF: "
	JA AUXILIARY
	
	PRINTN "0"
	JMP RETURN_0
	
	AUXILIARY:
	
	PRINTN "1"
	
	
	RETURN_0:
		
	MOV AH, 4CH
	INT 21H
	MAIN ENDP

END MAIN
