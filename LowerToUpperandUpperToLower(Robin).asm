INCLUDE "EMU8086.INC"

.MODEL SMALL
.STACK 100H

.DATA



.CODE

MAIN PROC
	
	MOV AH, 1
	INT 21H
	
	CMP AL, 60H
	JG TO_UPPER
	
	CMP AL, 5BH
	JL TO_LOWER
	
	
	TO_UPPER:
	
	SUB AL, 32
	
	JMP DISPLAY 
	
	
	TO_LOWER:
	
	ADD AL, 32  
	
	
	DISPLAY:
	
	PRINTN
	
	MOV AH, 2
	MOV DL, AL
	INT 21H
	
	
	MOV AH, 4CH
	INT 21H
	
	MAIN ENDP

END MAIN